let sayi = prompt("Lütfen 1 ile 5 sayi giriniz");

switch(sayi){

    case "1": console.log("Seçtiğiniz sayi 1");
    break;

    case "2": console.log("Seçtiğiniz sayi 2");
    break;

    case "3": console.log("Seçtiğiniz sayi 3");
    break;

    case "4": console.log("Seçtiğiniz sayi 4");
    break;

    case "5": console.log("Seçtiğiniz sayi 5");
    break;

    default: console.log("Girilen sayi 1 ile 5 arasında olmalıdır");
}
